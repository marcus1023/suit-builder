function scheduleA(event) {
    let frontLeftArm = document.getElementById('front-left-arm')
    if(this.options[this.selectedIndex].text === 'Red'){
      frontLeftArm.style.filter = 'hue-rotate(144deg)'
      console.log(frontLeftArm)
    }
    if(this.options[this.selectedIndex].text === 'Green'){
      frontLeftArm.style.filter = 'hue-rotate(275deg)'
      console.log(frontLeftArm)
    }
    if(this.options[this.selectedIndex].text === 'Blue'){
      frontLeftArm.style.filter = 'hue-rotate(15deg)'
      console.log(frontLeftArm)
    }
}
